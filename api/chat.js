import Anthropic from '@anthropic-ai/sdk';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const client = new Anthropic(); // reads ANTHROPIC_API_KEY from the environment

// Rate limit per IP, in memory: 20 requests / 10 min per instance. Enough at
// this scale — the real hard limit is the workspace spend limit.
const hits = new Map();
const WINDOW_MS = 10 * 60 * 1000, MAX_HITS = 20;
function rateLimited(ip) {
  const now = Date.now();
  const arr = (hits.get(ip) || []).filter(t => now - t < WINDOW_MS);
  arr.push(now); hits.set(ip, arr);
  return arr.length > MAX_HITS;
}

// ── Knowledge ──────────────────────────────────────────────────────────
// knowledge.json ({url,title,description,source,date}[]) is generated by a
// separate pipeline and lives at the repo root. vercel.json's
// functions.includeFiles bundles it into this function's deployment; the
// candidate paths below also cover a manual copy into api/ and a
// KNOWLEDGE_PATH override for local testing against a fixture.
function loadKnowledge() {
  const candidates = [
    process.env.KNOWLEDGE_PATH,
    path.join(process.cwd(), 'knowledge.json'),
    path.join(__dirname, 'knowledge.json'),
    path.join(__dirname, '..', 'knowledge.json'),
  ].filter(Boolean);
  for (const p of candidates) {
    try { return JSON.parse(fs.readFileSync(p, 'utf8')); } catch { /* try next candidate */ }
  }
  console.error('chat: knowledge.json not found, tried:', candidates);
  return [];
}

const KNOWLEDGE = loadKnowledge();
const ALLOWED_URLS = new Set(KNOWLEDGE.map(i => i.url));

const KNOWLEDGE_BLOCK = KNOWLEDGE
  .map(i => `- [${i.title}](${i.url}) — ${i.source}, ${i.date}: ${i.description}`)
  .join('\n');

